# Задание 2.7
# 1. Считать с клавиатуры произвольную строку.
# 2. Заменить в первой половине строки все символы "!" на "%".
# 3. Вывести количество замененных символов.

print("Задание 2.7")

# 1. Считываем произвольную строку с клавиатуры
input_string = input("Введите произвольную строку: ")

# Находим длину строки
length = len(input_string)

# Вычисляем индекс середины (первая половина)
# Если длина нечетная, первая половина будет меньше (целочисленное деление)
mid_index = length // 2

# Счетчик замен
replacement_count = 0

# Создаем новый список символов для построения результата
# (строки неизменяемы, поэтому работаем через список символов)
result_chars = []

# Проходим по всем символам строки по индексу
for i in range(length):
    current_char = input_string[i]
    
    # Проверяем, находится ли символ в первой половине
    if i < mid_index:
        # Если символ в первой половине и это "!", заменяем на "%"
        if current_char == "!":
            result_chars.append("%")
            replacement_count += 1
        else:
            result_chars.append(current_char)
    else:
        # Вторая половина строки остается без изменений
        result_chars.append(current_char)

# Собираем итоговую строку из списка символов
result_string = "".join(result_chars)

# 2. Выводим измененную строку
print(f"Измененная строка: {result_string}")

# 3. Выводим количество замен
print(f"Количество замененных символов: {replacement_count}")

