# Задание 1.4
# Считать с клавиатуры непустую произвольную последовательность целых чисел.
# Найти:
# i. Сумму всех чисел последовательности (используя while)
# ii. Количество всех чисел последовательности (используя while)

print("Задание 1.4")

total_sum = 0
count_numbers = 0

print("Вводите числа по одному. Нажмите Enter без ввода числа для завершения:")

while True:
    user_input = input("Введите число: ")
    
    # Проверка на завершение ввода (пустая строка)
    if user_input == "":
        break
    
    # Преобразование в целое число
    try:
        number = int(user_input)
        total_sum += number
        count_numbers += 1
    except ValueError:
        print("Ошибка: введите целое число или Enter для выхода.")

print(f"i. Сумма всех чисел последовательности: {total_sum}")
print(f"ii. Количество всех чисел последовательности: {count_numbers}")
