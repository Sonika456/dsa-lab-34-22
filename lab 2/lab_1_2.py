# Задание 1.2
# Считать с клавиатуры три произвольных числа, вывести в консоль те числа, 
# которые попадают в интервал [1, 50].

print("Задание 1.2")

# Ввод трех чисел
val1 = int(input("Введите первое число: "))
val2 = int(input("Введите второе число: "))
val3 = int(input("Введите третье число: "))

# Проверка попадания в интервал [1, 50]
# Используем список для удобного перебора, но ввод был раздельный
numbers = [val1, val2, val3]
print("Числа в интервале [1, 50]:")
for num in numbers:
    if 1 <= num <= 50:
        print(num)
print()
